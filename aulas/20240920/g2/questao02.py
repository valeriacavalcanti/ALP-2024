num1 = int(input('Primeiro valor: '))
num2 = int(input('Segundo valor: '))

soma = num1 + num2
subtracao = num1 - num2
multiplicacao = num1 * num2
divisao = num1 / num2

# exibir
print(f'{num1} + {num2} = {soma}')
print(f'{num1} - {num2} = {subtracao}')
print(f'{num1} x {num2} = {multiplicacao}')
print(f'{num1} / {num2} = {divisao:.2f}')
