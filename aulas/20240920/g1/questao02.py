num1 = int(input('Primeiro número: '))
num2 = int(input('Segundo número: '))

soma = num1 + num2
subtracao = num1 - num2
multiplicacao = num1 * num2
divisao = num1 / num2

print(f'{num1} + {num2} = {soma}')
print(f'{num1} - {num2} = {subtracao}')
print(f'{num1} * {num2} = {multiplicacao}')
print(f'{num1} / {num2} = {divisao:.4f}')
